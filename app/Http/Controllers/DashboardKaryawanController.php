    public function totalPengajuan()
    {
        $results = DB::connection()->select("
            SELECT MONTH(pst_masapelaksanaan) as bulan, COUNT(*) as total_pengajuan 
            FROM pengajuan_surat_tugas 
            GROUP BY bulan
        ");
    
        return $results;
    }
    
